numbers = (1, 2, 3, 4, 5)
more_numbers = (6, 7, 8)

# Concatenation
concatenated_tuple = numbers + more_numbers

# Repetition
repeated_tuple = numbers * 2

# Slicing
sliced_tuple = numbers[1:4]

print("Concatenated tuple:", concatenated_tuple)
print("Repeated tuple:", repeated_tuple)
print("Sliced tuple:", sliced_tuple)
