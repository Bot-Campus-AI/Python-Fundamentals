# Various examples demonstrating the use of integers
a = 10
b = 3

# Basic arithmetic operations
int_sum = a + b  # Addition
int_diff = a - b  # Subtraction
int_prod = a * b  # Multiplication
int_div = a // b  # Integer division
modulus = a % b  # Modulus

print("Sum:", int_sum)
print("Difference:", int_diff)
print("Product:", int_prod)
print("Integer Division:", int_div)
print("Modulus:", modulus)
