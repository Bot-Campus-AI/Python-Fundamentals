# Various examples demonstrating the use of floats
a = 10.5
b = 4.2

# Basic arithmetic operations
float_sum = a + b  # Addition
float_diff = a - b  # Subtraction
float_prod = a * b  # Multiplication
float_div = a / b  # Division

print("Sum:", float_sum)
print("Difference:", float_diff)
print("Product:", float_prod)
print("Division:", float_div)
