# Various examples demonstrating the use of booleans
is_raining = True
is_sunny = False

# Logical operations
and_operation = is_raining and is_sunny  # False
or_operation = is_raining or is_sunny  # True
not_operation = not is_raining  # False

print("AND operation:", and_operation)
print("OR operation:", or_operation)
print("NOT operation:", not_operation)
